package q18.abstracter.it;

public abstract class Parent //abstract class with 3 abstract methods
{

	abstract String upperCaseConv(String x); //abstract method for converting an string to all uppercase characters
	
	abstract boolean isStringUpperCase(String x); //abstract method for checking if a string has an uppercase character
	
	abstract void convertString(String x); //abstract method for converting a string to an integer data type
	
}
