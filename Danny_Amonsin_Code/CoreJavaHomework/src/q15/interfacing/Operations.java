package q15.interfacing;

public interface Operations //defined as interface by interface keyword
{
	
	void addition(int x, int y); //abstract addition method, parameter takes in a value of x and y integers
	void subtraction(int x, int y); //abstract subtraction method, parameter takes in a value of x and y integers
	void multiplication(int x, int y); //abstract multiplication method, parameter takes in a value of x and y integers
	void division(int x, int y); //abstract division method, parameter takes in a value of x and y integers
	
}
